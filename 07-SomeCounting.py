# Exercise 7: Some Counting - 20 Marks
# Various counting loops

# Loop 1: Count up from 0 to 50 in increments of 1
print("Counting from 0 to 50:")
for i in range(0, 51):
    print(i, end=" ")
print("\n")

# Loop 2: Count down from 50 to 0 in decrements of 1
print("Counting from 50 to 0:")
for i in range(50, -1, -1):
    print(i, end=" ")
print("\n")

# Loop 3: Count up from 30 to 50 in increments of 1
print("Counting from 30 to 50:")
for i in range(30, 51):
    print(i, end=" ")
print("\n")

# Loop 4: Count down from 50 to 10 in decrements of 2
print("Counting from 50 to 10 (by 2s):")
for i in range(50, 9, -2):
    print(i, end=" ")
print("\n")

# Loop 5: Count up from 100 to 200 in increments of 5
print("Counting from 100 to 200 (by 5s):")
for i in range(100, 201, 5):
    print(i, end=" ")
print()